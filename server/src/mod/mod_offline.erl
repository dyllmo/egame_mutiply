-module(mod_offline).

-export([
	do/1,		%%-----玩家下线
	clean/1		%%-----处理玩家下线
]).

-include("game.hrl").

%%--------------------------------------------------------------------------------------------------------------
%% @doc
%% 玩家下线
%% @end
%%--------------------------------------------------------------------------------------------------------------
do (State) ->
	Sid = State #client_state.sid,
	cluster:sid_call(Sid,?MODULE,clean,[State]),
	ok.
	
%%--------------------------------------------------------------------------------------------------------------
%% @doc
%% 处理玩家下线
%% @end
%%--------------------------------------------------------------------------------------------------------------
clean (_State) ->
	ok.